package com.gloria.models;

public class Producto {
    private String tamPro;
    private String codPro;
    private String nomPro;
    private String desPro;
    private double prePro;
    private String tipoPro;

    // Getters y Setters para acceder a los atributos
    public String getTamPro() {
        return tamPro;
    }
    public void setTamPro(String tamPro) {
        this.tamPro = tamPro;
    }

    public String getCodPro() {
        return codPro;
    }
    public void setCodPro(String codPro) {
        this.codPro = codPro;
    }

    public String getNomPro() {
        return nomPro;
    }
    public void setNomPro(String nomPro) {
        this.nomPro = nomPro;
    }

    public String getDesPro() {
        return desPro;
    }
    public void setDesPro(String desPro) {
        this.desPro = desPro;
    }

    public double getPrePro() {
        return prePro;
    }
    public void setPrePro(double prePro) {
        this.prePro = prePro;
    }

    public String getTipoPro() {
        return tipoPro;
    }
    public void setTipoPro(String tipoPro) {
        this.tipoPro = tipoPro;
    }
}

